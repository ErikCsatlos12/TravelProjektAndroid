package com.example.myapplication;

public class Attraction {
    private String name;
    private String city;
    private double price;
    private double rating;

    public Attraction(String name, String city, double price, double rating) {
        this.name = name;
        this.city = city;
        this.price = price;
        this.rating = rating;
    }


    public String getName() {
        return name;
    }

    public String getCity() {
        return city;
    }

    public double getPrice() {
        return price;
    }

    public double getRating() {
        return rating;
    }












}
